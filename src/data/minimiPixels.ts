// ============================================

/** 2D 색상 배열을 box-shadow 문자열로 변환 */
function gridToPixels(grid: (string | null)[][]): string {
    const shadows: string[] = [];
    for (let y = 0; y < grid.length; y++) {
        for (let x = 0; x < grid[y].length; x++) {
            const color = grid[y][x];
            if (color) {
                shadows.push(`${x}px ${y}px ${color}`);
            }
        }
    }
    return shadows.join(",");
}

